def create_simple_digital_twin():
    if not os.path.exists(root_dir):
        os.makedirs(root_dir)
        print(f"✈️ '{root_dir}' 격납고 건설 완료 (심플 Ver.)")

    for folder, files in structure.items():
        folder_path = os.path.join(root_dir, folder)
        if not os.path.exists(folder_path):
            os.makedirs(folder_path)
        
        for filename_raw, yaml_content in files.items():
            clean_filename = filename_raw.replace("[[", "").replace("]]", "")
            file_path = os.path.join(folder_path, f"{clean_filename}.md")
            
            content = f"""---
{yaml_content}
상태: 가동중
작성일: 2025-11-18
---

# {filename_raw}

## 1. 핵심 내용
(여기에 내용을 입력하세요)
"""
            with open(file_path, "w", encoding="utf-8") as f:
                f.write(content)
            
    print("✅ 케어온 시스템 구축 완료. (직관적 폴더명 + 비행기 메커니즘)")

